package simple.methods;

public class GettersAndSetters {

    private int age = 35;

    private String name = "Stayko";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
